#Estructura condicional If - else

# Sintaxis
# if condicion:
#    instucciones a ejecutarse si la condicion es verdadera
# else
#   instrucciones a ejecutar si la condicion es falsa

#Ejemplo
edad = 25

if edad < 18:
    print("La persona es menor de edad")
else:
    print("La persona es mayor o igual que edad")