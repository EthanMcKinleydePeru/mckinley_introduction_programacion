#Calcular la edad de una persona a partir de su ano de nacimiento

#Defini una variable que reciba el numero de ano de nacimiento de la persona
year_birthday = input("Ingresa el ano de tu nacimiento: ")

#Operacion de calculo de edad: ano_actual - ano_nacimiento
edad = 2025 - int(year_birthday)

#Visualiza la edad
print(f"La edad de la persona es: {edad}")
