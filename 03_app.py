#Variables

#Los variables son palabras especificas que utilizamos para asignar y almacenar valores especificos que podemos recuperar en cualquier momento.

#Variable nombre
nombre = "Ethan"

telefono = 997654111

name = "Alberto"

#Visualizando el contenido de las variables
print(nombre)
print(telefono)
print(name)


#Jugando con variables
age = 30
print(age)
print(f"Edad: {age}")

district = "San Luis"
print(district)
print(f"Nombre de Distrito: {district}")

price = 45.99
print(price)
print(f"El precio es: {price}")

is_active_client = True
print(is_active_client)
print(f"Esta activo? {is_active_client}")